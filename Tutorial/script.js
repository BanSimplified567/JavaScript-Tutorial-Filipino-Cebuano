/*
# Shorthand Operators
-Performs an arithmetic operator to a variables and assigns it to itself.

-|----------------------------------------------------|
-|  Operator   |       Usage       |    Same As       |
-|     +=      |      x += 5       |   x = x + 5      |
-|     -=      |      x -= 5       |   x = x - 5      |
-|     *=      |      x *= 5       |   x = x * 5      |
-|     /=      |      x /= 5       |   x = x / 5      |
-|    **=      |      x **= 2      |   x = x ** 2     |
-|     %=      |      x %= 5       |   x = x % 5      |
-|----------------------------------------------------|
*/

/*
# Increment / Decrement
- Adds / Subtracts 1 to a variable

-|----------------------------------------------------|
-|  Operator   |       Usage       |   Description    |
-|     ++      |      ++x or x++   |      Adds 1      |
-|     --      |      --x or x--   |   Subtracts 1    |
-|----------------------------------------------------|

// Prefix
let x = 0;

console.log(++x); // Output: 1
console.log(x); // Output: 1
*/

/*
// Postfix
*/
let x = 0;

console.log(x++);
console.log(x++);
